package models;

/**
 *
 * @author LENOVO
 */
public class Product {
    private int id;
    private String name;
    private int price;
    private String image;
    private String category;

    public Product() {
    }

    public Product(int id, String name, int price, String image, String category) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.image = image;
        this.category = category;
    }

    public Product(String name, int price, String image, String category) {
        this.name = name;
        this.price = price;
        this.image = image;
        this.category = category;
    }

    public Product(int id, String name, int price, String category) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.category = category;
    }
    
    
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

   
    public String getImage() {
        return image;
    }

    public void setImgae(String image) {
        this.image = image;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    @Override
    public String toString() {
        return "Product{" + "id=" + id + ", name=" + name + ", price=" + price + ", imgae=" + image + ", category=" + category + '}';
    }
    
    
}
